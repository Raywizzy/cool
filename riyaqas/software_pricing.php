<script>
// Define the currency conversion rates and symbols
const conversionRates = {
  USD: { symbol: '$', rate: 1 },
  EUR: { symbol: '€', rate: 0.85 },
  NGN: { symbol: '₦', rate: 774 },
  GBP: { symbol: '£', rate: 0.75 },
};

// Function to update the prices and links based on the selected currency
function updatePrices(selectedCurrency) {
  const pricingSections = document.querySelectorAll('.single-pricing');
  pricingSections.forEach((section) => {
    const usdPriceElement = section.querySelector('.price');
    if (usdPriceElement) {
      const usdPrice = parseFloat(usdPriceElement.getAttribute('data-usd-price'));
      const convertedPrice = usdPrice * conversionRates[selectedCurrency].rate;
      const symbol = conversionRates[selectedCurrency].symbol;

      usdPriceElement.textContent = `${symbol}${convertedPrice.toFixed(2)}`;

      const getStartedLink = section.querySelector('.btn');
      if (getStartedLink) {
        const tier = section.querySelector('.title').textContent;
        getStartedLink.href = `checkout.php?tier=${tier}&price=${convertedPrice.toFixed(2)}&currency=${selectedCurrency}`;
      }
    }
  });
}

// Get all currency buttons and add click event listeners
const currencyButtons = document.querySelectorAll('.currency-button');
currencyButtons.forEach((button) => {
  button.addEventListener('click', () => {
    const selectedCurrency = button.getAttribute('data-currency');
    updatePrices(selectedCurrency);
  });
});

// Initialize with default currency (USD)
updatePrices('USD');
</script>

<script>
    document.addEventListener("DOMContentLoaded", function () {
        const cartContent = document.getElementById("cart-content");
        const cartTotal = document.getElementById("cart-total");
        const cartItemsList = document.getElementById("cart-items-list");
        const emptyCartButton = document.getElementById("empty-cart-button");

        const shoppingCart = {
            items: [],
            currencySymbol: "$",
        };

        // Load Cart Data from localStorage
        const savedCartData = localStorage.getItem('shoppingCart');
        if (savedCartData) {
            shoppingCart.items = JSON.parse(savedCartData);
            renderCartItems();
            updateCartSubtotal();
        }

        // Function to update the shopping cart subtotal
        function updateCartSubtotal() {
            let subtotal = 0;
            for (const item of shoppingCart.items) {
                subtotal += item.quantity * item.price;
            }

            cartTotal.querySelector(".amount").textContent = `${shoppingCart.currencySymbol}${subtotal.toFixed(2)}`;
        }

        // Function to render cart items
        function renderCartItems() {
            cartItemsList.innerHTML = ""; // Clear the existing items

            for (const item of shoppingCart.items) {
                const cartItem = document.createElement("li");
                cartItem.innerHTML = `
                    <div class="media">
                        <div class="media-left">
                            <img src="${item.image}" alt="Product Image">
                        </div>
                        <div class="media-body">
                            <a class="title" href="#">${item.title}</a>
                            <p>Quantity: ${item.quantity}</p>
                            <span class="price">${shoppingCart.currencySymbol}${(item.quantity * item.price).toFixed(2)}</span>
                        </div>
               
                    </div>
                `;

                cartItemsList.appendChild(cartItem);
            }

            // Add click event listeners to "Remove" buttons
            const removeItemButtons = cartItemsList.querySelectorAll(".remove-product");
            removeItemButtons.forEach((button) => {
                button.addEventListener("click", (event) => {
                    event.preventDefault();
                    const title = event.target.getAttribute("data-title");
                    removeItemFromCart(title);
                });
            });
        }

        // Function to add an item to the shopping cart
        function addToCart(title, price, image) {
            const existingItem = shoppingCart.items.find((item) => item.title === title);

            if (existingItem) {
                existingItem.quantity += 1;
            } else {
                shoppingCart.items.push({ title, price, image, quantity: 1 });
            }

            renderCartItems();
            updateCartSubtotal();

            // Update localStorage with the current cart items
            localStorage.setItem('shoppingCart', JSON.stringify(shoppingCart.items));
        }

        // Function to empty the shopping cart
        function emptyCart() {
            shoppingCart.items = [];
            renderCartItems();
            updateCartSubtotal();

            // Update localStorage to remove cart data
            localStorage.removeItem('shoppingCart');
        }

        // Add click event listeners to "Add To Cart" buttons
        const addToCartButtons = document.querySelectorAll(".single-pricing .btn");
        addToCartButtons.forEach((button) => {
            button.addEventListener("click", (event) => {
                event.preventDefault();
                const pricingSection = event.target.closest(".single-pricing");
                const title = pricingSection.querySelector(".title").textContent;
                const price = parseFloat(pricingSection.querySelector(".price").textContent.replace("$", ""));
                const image = pricingSection.querySelector("img").src;

                addToCart(title, price, image);
            });
        });

        // Add click event listener to the "Empty Cart" button
        emptyCartButton.addEventListener("click", emptyCart);
    });
</script>

