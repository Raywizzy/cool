<script>
// Define the currency conversion rates and symbols
const conversionRates = {
  USD: { symbol: '$', rate: 1 },
  EUR: { symbol: '€', rate: 0.85 },
  NGN: { symbol: '₦', rate: 774 },
  GBP: { symbol: '£', rate: 0.75 },
};

// Function to update the prices and links based on the selected currency
function updatePrices(selectedCurrency) {
  const pricingSections = document.querySelectorAll('.single-pricing');
  pricingSections.forEach((section) => {
    const usdPriceElement = section.querySelector('.price');
    if (usdPriceElement) {
      const usdPrice = parseFloat(usdPriceElement.getAttribute('data-usd-price'));
      const convertedPrice = usdPrice * conversionRates[selectedCurrency].rate;
      const symbol = conversionRates[selectedCurrency].symbol;

      usdPriceElement.textContent = `${symbol}${convertedPrice.toFixed(2)}`;

      const getStartedLink = section.querySelector('.btn');
      if (getStartedLink) {
        const tier = section.querySelector('.title').textContent;
        getStartedLink.href = `checkout.php?tier=${tier}&price=${convertedPrice.toFixed(2)}&currency=${selectedCurrency}`;
      }
    }
  });
}

// Get all currency buttons and add click event listeners
const currencyButtons = document.querySelectorAll('.currency-button');
currencyButtons.forEach((button) => {
  button.addEventListener('click', () => {
    const selectedCurrency = button.getAttribute('data-currency');
    updatePrices(selectedCurrency);
  });
});

// Initialize with default currency (USD)
updatePrices('USD');
</script>


